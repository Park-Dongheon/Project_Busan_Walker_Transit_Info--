// src/domains/map/model/cachePolicy.ts

/**
 * cachePolicy.ts (지도 조회 캐시 정책 상수)
 * 
 * 역할/목적:
 * - map 도메인에서 사용하는 캐시 수명 정책을 한 곳에서 관리
 * - 조회 유형별 staleTime, gcTime, 파생 데이터 캐시 크기 제한을 중앙에서 정의하여
 *   훅과 모델 계층이 동일한 기준을 공유
 * 
 * 책임 범위:
 * - React Query 기반 조회 캐시의 신선도(staleTime)와 보관 시간(gcTime)을 정의
 * - 대중교통 파생 데이터의 메모리 캐시 상한값을 정의
 * 
 * 설계 정책:
 * - 현재 파일은 정책 상수의 SSOT 역할만 담당
 * - 런타임 분기, 조회 실행, 캐시 갱신 로직은 포함하지 않음
 * - 값의 의미가 바뀌면 호출부 훅의 동작 체감도 함께 바뀌므로,
 *   상수는 단순 숫자가 아니라 UX와 성능 사이의 합의된 정책으로 다뤄야 함
 * 
 * 동작 포인트:
 * - 핀 목록은 상대적으로 자주 변하는 화면 상호작용을 고려해 짧은 staleTime을 사용
 * - 상세 조회는 사용자가 같은 관광지를 반복 열어볼 가능성을 고려해 더 긴 보관 시간을 사용
 * - 파생 데이터 캐시는 재계산 비용과 메모리 사용량 사이의 균형을 위해 최대 개수를 제한
 * 
 * 운영 포인트:
 * - staleTime을 늘리면 네트워크 요청은 줄지만 오래된 데이터가 남을 가능성이 커짐
 * - gcTime을 늘리면 재진입 성능은 좋아지지만 메모리 사용량이 증가
 * - 파생 캐시 최대 크기를 조정할 때는 지도 이동 빈도, 관광지 재방문 패턴, 브라우저 메모리 사용량을 함께 점검
 * 
 * 주의:
 * - 이 값들은 도메인 정책값이므로, 개별 훅에서 임의 숫자를 하드코딩하면 캐시 동작이 화면마다 달라짐
 * - React Query 버전이나 캐시 전략이 바뀌면 staleTime/gcTime의 의미를 먼저 확인한 뒤 이 파일과 호출부를 함께 점검
 */

/* bbox 기반 핀 목록 조회가 "Fresh 값"으로 간주되는 시간 */
export const MAP_PINS_STALE_TIME = 60_000

/* 핀 목록 조회 결과를 메모리에 유지하는 시간 */
export const MAP_PINS_GC_TIME = 5 * 60_000

/**
 * 선택한 관광지 상세 조회를 "Fresh 값"으로 간주하는 시간
 * 
 * 상세 정보는 목록보다 재사용 가능성이 높아, 핀 목록보다 더 긴 Fresh 정책을 사용
 */
export const MAP_DETAIL_STALE_TIME = 7 * 60_000

/**
 * 관광지 상세 조회 결과를 메모리에 유지하는 시간
 * 
 * 사용자가 상세 패널을 반복적으로 열고 닫는 흐름에서 재조회 비용을 줄이기 위한 값
 */
export const MAP_DETAIL_GC_TIME = 20 * 60_000

/**
 * 대중교통 파생 데이터의 LRU 캐시 최대 항목 수
 * 
 * 파생 계산 결과는 네트워크 응답이 아니라 클라이언트 계산 산출물이므로,
 * 무제한 보관 대신 상한을 두어 메모리 사용량을 통제
 */
export const MAP_TRANSIT_DERIVED_CACHE_MAX = 40